    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        try {
            int baris = tblBarang.getSelectedRow();
            crud.bukakoneksi();
            barang = crud.readById(Integer.parseInt(tblBarang.getValueAt(baris, 0).toString()));
            crud.tutupkoneksi();
            txtId.setText(Integer.toString(barang.getId()));
            txtNama.setText(barang.getNama());
            txtMerk.setText(barang.getMerk());
            txtAsal_brg.setText(barang.getAsal_brg());
            txtPTimpor.setText(barang.getPt_impor());
            txtPTekspor.setText(barang.getPt_ekspor());
            txtNoImpor.setText(barang.getNo_impor());
            txtNoEkspor.setText(barang.getNo_ekspor());
            txtHarga.setText(barang.getHarga());
            txtJml.setText(barang.getJml_brg());
 
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Ambil data gagal" + ex.getMessage());
        }
    }//GEN-LAST:event_tblBarangMouseClicked

