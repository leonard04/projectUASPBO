    public void getAllData() {
        try {
            model.setRowCount(0);
            crud.bukakoneksi();
            ArrayList<Barang> list = crud.read();
            System.out.println(list.size());
            list.forEach((data) -> {
                model.addRow(new Object[]{
                    data.getId(),
                    data.getNama(),
                    data.getMerk(),
                    data.getAsal_brg(),
                    data.getPt_impor(),
                    data.getPt_ekspor(),
                    data.getNo_impor(),
                    data.getNo_ekspor(),
                    data.getHarga(),
                    data.getJml_brg()
                });
            });
            crud.tutupkoneksi();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Mengambil Data" + ex.getMessage());
        }
    }

