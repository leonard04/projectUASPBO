    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        Barang barang;
        if (status.equals("add")) {
            try {
                if (txtNama.getText().isEmpty() || txtMerk.getText().isEmpty() || txtAsal_brg.getText().isEmpty()
                        || txtPTimpor.getText().isEmpty() || txtPTekspor.getText().isEmpty() || txtNoImpor.getText().isEmpty()
                        || txtNoEkspor.getText().isEmpty() || txtHarga.getText().isEmpty() || txtJml.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukakoneksi();
                    barang = new Barang();
                    barang.setNama(txtNama.getText());
                    barang.setMerk(txtMerk.getText());
                    barang.setAsal_brg(txtAsal_brg.getText());
                    barang.setPt_impor(txtPTimpor.getText());
                    barang.setPt_ekspor(txtPTekspor.getText());
                    barang.setNo_impor(txtNoImpor.getText());
                    barang.setNo_ekspor(txtNoEkspor.getText());
                    barang.setHarga(txtHarga.getText());
                    barang.setJml_brg(txtJml.getText());
                    crud.create(barang);
                    getAllData();
                    readData();
                    crud.tutupkoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan: error, " + ex.getMessage());
            }
        } else {
            try {
                if (txtNama.getText().isEmpty() || txtMerk.getText().isEmpty() || txtAsal_brg.getText().isEmpty()
                        || txtPTimpor.getText().isEmpty() || txtPTekspor.getText().isEmpty() || txtNoImpor.getText().isEmpty()
                        || txtNoEkspor.getText().isEmpty() || txtHarga.getText().isEmpty() || txtJml.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Data harus diisi semua");
                } else {
                    crud.bukakoneksi();
                    barang = new Barang();
                    barang.setId(Integer.parseInt(txtId.getText()));
                    barang.setNama(txtNama.getText());
                    barang.setMerk(txtMerk.getText());
                    barang.setAsal_brg(txtAsal_brg.getText());
                    barang.setPt_impor(txtPTimpor.getText());
                    barang.setPt_ekspor(txtPTekspor.getText());
                    barang.setNo_impor(txtNoImpor.getText());
                    barang.setNo_ekspor(txtNoEkspor.getText());
                    barang.setHarga(txtHarga.getText());
                    barang.setJml_brg(txtJml.getText());
                    crud.update(barang);
                    getAllData();
                    readData();
                    crud.tutupkoneksi();
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan: error, "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btSimpanActionPerformed

